function createfigure(XData1,YData1,XData2,YData2,XData3,YData3,XData4,YData4,XData5,YData5,XData6,YData6,title)
%CREATEFIGURE(XData1, YData1, XData2, YData2, XData3, YData3, XData4, YData4, XData5, YData5, XData6, YData6)
%  XDATA1:  line xdata
%  YDATA1:  line ydata
%  XDATA2:  line xdata
%  YDATA2:  line ydata
%  XDATA3:  line xdata
%  YDATA3:  line ydata
%  XDATA4:  line xdata
%  YDATA4:  line ydata
%  XDATA5:  line xdata
%  YDATA5:  line ydata
%  XDATA6:  line xdata
%  YDATA6:  line ydata

% Create figure
figure1 = figure('Tag','MLearnAppExportedScatterPlot','Name','Scatter Plot');

% Create axes
axes1 = axes('Tag','MLearnAppExportedScatterPlotAxes','Parent',figure1);

% Create line
line(XData1,YData1,'DisplayName','Spondylolisthesis - Correct',...
    'Tag','MLearnAppScatterDataCorrectPrediction',...
    'Parent',axes1,...
    'MarkerSize',18,...
    'Marker','.',...
    'LineWidth',2,...
    'LineStyle','none',...
    'Color',[0.929 0.694 0.125]);

% Create line
line(XData2,YData2,'DisplayName','Spondylolisthesis - Incorrect',...
    'Tag','MLearnAppScatterDataIncorrectPrediction',...
    'Parent',axes1,...
    'MarkerSize',7.8,...
    'Marker','x',...
    'LineWidth',2,...
    'LineStyle','none',...
    'Color',[0.929 0.694 0.125]);

% Create line
line(XData3,YData3,'DisplayName','Normal - Correct',...
    'Tag','MLearnAppScatterDataCorrectPrediction',...
    'Parent',axes1,...
    'MarkerSize',18,...
    'Marker','.',...
    'LineWidth',2,...
    'LineStyle','none',...
    'Color',[0.85 0.325 0.098]);

% Create line
line(XData4,YData4,'DisplayName','Normal - Incorrect',...
    'Tag','MLearnAppScatterDataIncorrectPrediction',...
    'Parent',axes1,...
    'MarkerSize',7.8,...
    'Marker','x',...
    'LineWidth',2,...
