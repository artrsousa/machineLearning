    'Tag','MLearnAppScatterDataIncorrectPrediction',...
    'Parent',axes1,...
    'MarkerSize',7.8,...
    'Marker','x',...
    'LineWidth',2,...
    'LineStyle','none',...
    'Color',[0 0.447 0.741]);

% Create ylabel
ylabel('Var2','Interpreter','none');

% Create xlabel
